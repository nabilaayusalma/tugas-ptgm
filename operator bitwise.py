#python 3.7.1

a = 6
b = 3

#bitwise AND (&)

c = a & b
print('nilai :',a,)
print('nilai :',b,)
print('niai bitwise AND :',c,)

#bitwise OR (|)
c = a | b
print('nilai :',a,)
print('nilai :',b,)
print('niai bitwise OR :',c,)

#bitwise XOR (^)
c = a ^ b
print('nilai :',a,)
print('nilai :',b,)
print('niai bitwise XOR :',c,)

#bitwise NOT (~)
c = ~a
c = ~b
print('nilai :',a,)
print('nilai :',b,)
print('niai bitwise NOT :',c,)

#left-shift(>>)
c = a >> 2
print('nilai :',a,)
print('nilai :',b,)
print('niai bitwise left-shift :',c,)

#righ-shift(<<)
c = a << 2
print('nilai :',a,)
print('nilai :',b,)
print('niai bitwise righ-shift :',c,)